class dog:
     def __init__(self,name,color,age):
          self.name= name
          self.color=color
          self.age= age
Dawa =dog("Dawa","Brown",4)  
Nima =dog("Nima","Yellow",3)
Blacky =dog("Blacky","Black",1)

print(Dawa.age, Dawa.color,Dawa.name)
print(Nima.age, Nima.color,Nima.name)

print(Blacky.age, Blacky.color,Blacky.name)


       
                           